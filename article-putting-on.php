	<h1>Putting on the gloves can help in dropping pounds</h1>
	<h3>By Mike Organ</h3>
	<p>Frank Wood's clients are serious about fighting the battle of the bulge.  They take it literally.  Wood is a pro boxer and owner of Knockout Fitness, a workout facility in downtown Nashville.  Since he still is an active boxer, having recently signed a contract to fight Evander Holyfield, Wood has decided not to start training other fighters.  But, believing his intense training could benefit others, he started offering help for non-boxers.  "I am a boxer and what we do is teach boxing lessons for physical fitness," Wood said.  In other words, it's boxing without the bloody nose.</p>
	<p>"Basically, we take people and teach them to box," Wood said.  "All the way from the stance, to different punches, to hitting heavy bags, hitting hand pads and jumping rope.  Doing the things that fighters do to stay in condition."</p>
	<p>Wood has male and female clients ranging in ages from 13-60.  "I was looking for an atypical work out," said Carey Nelson Burch, one of Wood's most ardent clients.  "I didn't want to be stuck in a class with a bunch of people or want to have to motivate myself to work out.  I found this was a great way to lose weight, a great way to work out through my last pregnancy and get back on the ball right afterwards.  I just love the energy in that place."</p>
	<p>Training with Wood 3 times per week, Nelson Burch said boxing provides her a better workout than running every day like she did before.  "It's not monotonous," Nelson Burch said.  "You get strength training, you get cardiovascular training, and it's a great way to get out aggression.  It's a solid hour of nonstop activity."</p>
	<p>A session lasts 1 hour and can be done individually or in groups with classes offered weekdays in the mornings and at night or on Saturday morning.</p>
	<p>The intensity level of the workout is based on the individual.  It can be as mild as a 1 mile walk or as taxing as a 5 mile run.</p>
	<p>"We have every level," Wood said.  "We have people who you can't make tired.  They'll keep coming no matter how hard you work them.  We have people who aren't as capable, and then we have those who are total beginners who are building themselves up."</p>
	<p>For more information or to set up an appointment call Knockout Fitness at 615–255–1359.</p>

