	<h1>FIGHT CLUB</h1>
	<h2>Punch your way to a better body</h2>
	<h3>By Virginia Roberson</h3>
	<p>Tired of those monotonous aerobic classes where it seems like you spend more time watching the clock than working out?  Want something a little more challenging than the dance routine your cushy health club calls "kickboxing?"  If you're ready to really kick that ass into shape, maybe it's time to get help from the pros, like boxer Frank Wood at Nashville's Knockout Fitness.</p>
	<p>"They kick your butt here," says Carey Nelson Burch, 37, whose been training at Knockout Fitness for 3 years, but it's like a family setting, and Frank really knows what he is doing.  After all, he is a professional heavyweight boxer!"  Although Burch is learning actual boxing techniques such as punching, foot work, balance and stance, all she really cares about is that the program provides a great cardio workout and strength training.  This busy TV/film agent and mother of 2 children trains 3 days a week with Frank Wood.  "I don't have to worry about anybody hitting me, "she says.  "They don't spar in the classes here, and it beats aerobics that are more like dance classes.  This one really challenges me."</p>
	<p>Wood who is a retired professional boxer worries that some so-called kickboxing classes could lead to injury, because many instructors have no training in professional fighting. "Most of these classes are not taught by someone with a boxing background, so they may not be showing you the proper way to throw a punch."  You won't see anyone at Knockout Fitness training for the ring either.  Even though Wood is qualified to teach boxing, he doesn't encourage sparring.  "We teach the basics of boxing to people who want to get in shape," he says.  "Most of my students are professionals who are not looking to show up to work with a black eye."</p>

